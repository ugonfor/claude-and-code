def get_spinning_globe_frames(size=15):
    """Generate frames of a spinning ASCII globe."""
    import math
    frames_data = []
    num_frames = 12

    for f in range(num_frames):
        offset = f * (2 * math.pi / num_frames)
        lines = []
        for y in range(size):
            row = []
            for x in range(size * 2):
                # Map to sphere coordinates
                nx = (x - size) / size
                ny = (y - size / 2) / (size / 2)
                dist = math.sqrt(nx * nx + ny * ny)

                if dist > 1.0:
                    row.append(' ')
                elif dist > 0.95:
                    row.append('.')
                else:
                    # Longitude lines
                    lon = math.atan2(ny, nx) + offset
                    lat = math.asin(min(1, max(-1, dist)))

                    if abs(math.sin(lon * 4)) < 0.1:
                        row.append('|')
                    elif abs(math.sin(lat * 4)) < 0.15:
                        row.append('-')
                    else:
                        row.append(' ')
            lines.append(''.join(row))
        frames_data.append(lines)

    return frames_data


